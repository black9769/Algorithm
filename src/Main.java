import java.util.*;

public class Main {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int tc = sc.nextInt();
        for (int i = 0; i < tc; i++) {
            long a = sc.nextInt();
            long b = sc.nextInt();
            System.out.println(a + b);
        }
    }
}
